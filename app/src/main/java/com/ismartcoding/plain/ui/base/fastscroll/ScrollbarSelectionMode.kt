package com.ismartcoding.plain.ui.base.fastscroll

/**
 * Scrollbar selection modes.
 */
enum class ScrollbarSelectionMode {
    /**
     * Enable selection in the whole scrollbar and thumb
     */
    Full,
    /**
     * Enable selection in the thumb
     */
    Thumb,
    /**
     * Disable selection
     */
    Disabled
}